public class Hello {

	public static void main(String[] args) {
		//'System.out' : 출력을 나타냄
		//'print()' : 괄호 안에 전달된 값을 출력
		// ';' : 코드에서 하나의 명령이 끝날 때 마다 작성
		System.out.print("안녕하세요.");
		//print : 가로로 쭉 이어서 출력
		//println : 다음 출력문은 줄을 바꿔서 출력(엔터역할)
		
		System.out.print("반갑습니다.");
		System.out.println("환영합니다.");
		System.out.println("어서오세요. 점심시간");
		
		System.out.println("1");
		System.out.println("23456789");
		System.out.println("01234");
		System.out.println(1);
		System.out.println(01);
		//앞자리 수에 0을 넣기 위해선 ""안에 넣어야함
		//맨 앞자리에 0을 넣으면 무시됨
		System.out.println(true);
		//true = 사실, 맞음
		//sysout + 컨트롤 + 스페이스바 = system.out.println()자동완성
		
	}

}